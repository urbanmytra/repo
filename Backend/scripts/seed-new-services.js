const seedNewServices = async () => {
  console.log(' Starting to seed services...');

  try {
    const provider = await Provider.findOne({ status: 'active' });

    if (!provider) {
      console.error(' No active provider found. Please seed providers first.');
      console.log(' Run `npm run seed` to create sample providers.');
      await mongoose.connection.close();
      process.exit(1);
    }

    console.log(`  Assigning services to provider: ${provider.name} (${provider.email})`);

    let createdCount = 0;
    let skippedCount = 0;

    for (const serviceData of allServicesData) {
      const existingService = await Service.findOne({
        name: serviceData.name,
        provider: provider._id,
      });

      if (!existingService) {
        await Service.create({
          ...serviceData,
          provider: provider._id,
        });
        console.log(` Service created: ${serviceData.name}`);
        createdCount++;
      } else {
        console.log(`  Service already exists, skipping: ${serviceData.name}`);
        skippedCount++;
      }
    }

    console.log('\n Summary:');
    console.log(`   Total services: ${allServicesData.length}`);
    console.log(`   Created: ${createdCount}`);
    console.log(`   Skipped: ${skippedCount}`);
    console.log(' Services seeding completed successfully!');
  } catch (error) {
    console.error(' Error seeding services:', error);
  } finally {
    await mongoose.connection.close();
    process.exit(0);
  }
};

connectDB().then(() => {
  seedNewServices();
});